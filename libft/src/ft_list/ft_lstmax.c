#include "../../inc/ft_list.h"

int	ft_lstmaxint(t_list *lst)
{
	int	max;

	max = -2147483648;
	while (lst)
	{
		if (max < *(int *)lst->content)
		{
			max = *(int *)lst->content;
		}
		lst = lst->next;
	}
	return (max);
}
